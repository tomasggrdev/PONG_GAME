    private void leerRecords(){
        try
        {   
            // leyendo archivo
            FileInputStream file = new FileInputStream("src/pong_game/juego/puntajes.txt");
            ObjectInputStream in = new ObjectInputStream(file);
              
            // deserializando archivo
            RegistroPuntaje object1 = (RegistroPuntaje)in.readObject();
              
            in.close();
            file.close();
            
            
            
            for(Map.Entry<String,Long> entry : object1.getRecords().entrySet()) {
                String key = entry.getKey();
                String value = String.valueOf(entry.getValue());
                
                lista.add(new Registro(Integer.valueOf(value),key));
            }
            Collections.sort(lista);
            int i = 1;
            for( Registro r : lista){
                listaPuntajes.addElement( i + " " +r.nombre + ": " + r.puntaje);
                i++;
            }
            
            jList2.setModel(listaPuntajes);
            
              
            
        } catch(IOException e)
        {
            System.out.println(e.getMessage());;
        } catch(ClassNotFoundException e)
        {
            System.out.println(e.getMessage());;
        }
    }

